/*
Var是变量
1.关于程序当中的数据？
	开发软件是为了解决问题
	而现实世界当中，很多问题都是使用数据进行描述的，
	所有软件执行过程中最主要的是对数据的处理

	在Java中表示数据的称为“字面量”。
	整数型（int long short ）字面量：1 2 3 4  4232
	浮点型（float）字面量：1.2 2123.2 3.1415
	字符型（char）字面量：‘a’ '中' ‘c’
	字符串字面量：“abc”  "比亚迪"

*/
public class VarTest{
	public static void main(String[] args){
		System.out.println(100);//整型
		System.out.println(3.14);//浮点型
		System.out.println(true);//布尔型 真
		System.out.println(false);//布尔型 假
		System.out.println('a');//字符型
		System.out.println('中');//字符型
		System.out.println("abc");//字符串型
		System.out.println("国");//双引号字符串 单引号字符型
		}
}